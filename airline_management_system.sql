CREATE DATABASE Airline_management_system;
USE Airline_management_system;
CREATE TABLE employee (
    EmployeeID INT PRIMARY KEY,
    EmployeeName VARCHAR(255),
    JobTitle VARCHAR(255),
    NumOfFlights INT,
    TotalFlights INT
);

CREATE TABLE flights (
    FlightID VARCHAR(255) PRIMARY KEY,
    FlightDuration INT,
    OccupancyRatE INT,
    FlightTime DECIMAL(4, 2)
);

CREATE TABLE attends (
    FlightID VARCHAR(255),
    EmployeeID INT,
    FOREIGN KEY (FlightID) REFERENCES flights (FlightID),
    FOREIGN KEY (EmployeeID) REFERENCES employee (EmployeeID)
);

CREATE TABLE arrival_airport (
    FlightID VARCHAR(255),
    AirportName VARCHAR(255),
    FOREIGN KEY (FlightID) REFERENCES flights (FlightID)
);

CREATE TABLE airport_info (
    AirportName VARCHAR(255),
    City VARCHAR(255),
    Country VARCHAR(255),
    AircraftCapacity INT
);

CREATE TABLE plane (
    AircraftID INT PRIMARY KEY,
    AircraftType VARCHAR(255)
);

CREATE TABLE passanger (
    PassangerID INT PRIMARY KEY,
    Name VARCHAR(255),
    Surname VARCHAR(255),
    Gender VARCHAR(255),
    Age INT
);

CREATE TABLE departure_airport (
    FlightID VARCHAR(255),
    AirportName VARCHAR(255),
    FOREIGN KEY (FlightID) REFERENCES flights (FlightID)
);

